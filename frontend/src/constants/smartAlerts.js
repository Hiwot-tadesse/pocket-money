// Get today's tip index (rotates daily)
export const getDailyTipIndex = () => {
  const start = new Date('2024-01-01').getTime();
  const now = Date.now();
  const daysSinceStart = Math.floor((now - start) / (1000 * 60 * 60 * 24));
  return daysSinceStart % FINANCIAL_TIPS.length;
};
